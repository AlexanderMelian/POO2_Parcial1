package parcial_practico;

public class Plancha {
	private Integer L, A, H;
	
	public Plancha(int L, int A, int H) {
		this.L = L;
		this.A = A;
		this.H = H;
	}

	public int getL() {
		return L;
	}

	public void setL(int l) {
		L = l;
	}

	public int getA() {
		return A;
	}

	public void setA(int a) {
		A = a;
	}

	public int getH() {
		return H;
	}

	public void setH(int h) {
		H = h;
	}
	
}